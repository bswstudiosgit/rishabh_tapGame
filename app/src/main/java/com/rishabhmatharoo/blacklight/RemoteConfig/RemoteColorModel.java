package com.rishabhmatharoo.blacklight.RemoteConfig;

public class RemoteColorModel {
    private String colour1;
    private String colour2;
    private String colour3;
    private String colour4;
    private String colour5;

    public RemoteColorModel() {
    }

    public RemoteColorModel(String colour1, String colour2, String colour3, String colour4, String colour5) {
        this.colour1 = colour1;
        this.colour2 = colour2;
        this.colour3 = colour3;
        this.colour4 = colour4;
        this.colour5 = colour5;
    }

    public String getColour5() {
        return colour5;
    }

    public void setColour5(String colour5) {
        this.colour5 = colour5;
    }

    public String getColour1() {
        return colour1;
    }

    public void setColour1(String colour1) {
        this.colour1 = colour1;
    }

    public String getColour2() {
        return colour2;
    }

    public void setColour2(String colour2) {
        this.colour2 = colour2;
    }

    public String getColour3() {
        return colour3;
    }

    public void setColour3(String colour3) {
        this.colour3 = colour3;
    }

    public String getColour4() {
        return colour4;
    }

    public void setColour4(String colour4) {
        this.colour4 = colour4;
    }


}
